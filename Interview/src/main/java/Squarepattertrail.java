public class Squarepattertrail {

    public static void main(String args[])
    {
        int rows=4;
        int cols=5;


        for(int row=1;row<=rows;row++)
        {
            for(int col=1;col<=cols;col++)
            {
                System.out.print("( ");
            }
            System.out.println("");
        }
    }
}
